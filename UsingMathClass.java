public class UsingMathClass{
	public static void main(String[] args){
		System.out.println("The absolute value of -60 is " + Math.abs(-60));
		System.out.println("The greater number is " + Math.max(25,90));
		System.out.println("The smaller number is " + Math.min(25,90));
		System.out.println("The square root of 25 is " + Math.sqrt(25));
		System.out.println("The result of 6^2 is " + Math.pow(6,2));
		System.out.println("The random number is " + Math.random());
	}
}